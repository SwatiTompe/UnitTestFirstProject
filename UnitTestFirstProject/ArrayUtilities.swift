import Foundation


struct ArrayUtilities {
 
    static func findMax(_ nunbers:[Int]) -> Int? {
        return nunbers.max()
    }
    
    static func findMin(_ numbers:[Int]) -> Int? {
        return numbers.min()
    }
}
